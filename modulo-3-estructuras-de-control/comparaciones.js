// Comparaciones

// Igualdad
let a = 5
let b = "5"

// == Solo compara el valor
if (a == b) {
    console.log("a es igual a b")
}

// === Compara el valor y el tipo
if (a === b) {
    console.log("a es muy igual a b")
}

// Desigualdad
let c = 4
let d = "4"

// Diferentes en valor
if (c != d) {
    console.log("c es diferente a d")
}

// Diferentes en valor o en tipo
if (c !== d) {
    console.log("c es muy diferente a d")
}

let e = 10
let f = 5

// Mayor que
if (e > f) {
    console.log("e es mayor que f")
}

// Mayor o igual que
if (e >= f) {
    console.log("e es mayor o igual que f")
}

// Menor que
if (e < f) {
    console.log("e es menor que f")
}

// Menor o igual que
if (e <= f) {
    console.log("e es menor o igual que f")
}